# Number
num1 = 5        # Integer (Fixnum)
num2 = 5.5      # Float

puts "Números: #{num1}, #{num2}"

# String
str = "Hello, World!"
puts "String: #{str}"

# Array
arr = [1, "two", 3.0]
puts "Array: #{arr}"

# Hash
hash = { "name" => "Bob", "age" => 30 }
puts "Hash: #{hash}"

# Symbol
sym = :symbol
puts "Symbol: #{sym}"

# Boolean
t = true
f = false
puts "Booleanos: #{t}, #{f}"

# Nil
n = nil
puts "Nil: #{n}"

# Range
range = 1..10
puts "Range: #{range}"

# Uso do range em um loop
puts "Números no intervalo: "
range.each do |n|
  puts n
end


